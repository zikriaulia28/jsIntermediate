// Perbedaan let, var dan const

// var memiliki scope global jika dideklarasikan di luar fungsi dan scope lokal jika dideklarasikan di dalam fungsi, dapat di deklarasikan ulang dan nilai nya dapat di ubah

// let memiliki scope yang lebih terbatas dibandingkan var yaitu block, tidak dapat di deklarasikan ulang dan nilai nya dapat di ubah

// const memiliki scope yang sama seperti let namun nilainya tidak dapat diubah setelah dideklarasikan.
